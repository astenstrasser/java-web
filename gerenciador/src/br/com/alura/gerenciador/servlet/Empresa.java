package br.com.alura.gerenciador.servlet;

public class Empresa {

	private int id;
	private String name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNome() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
